		<div class="inner">
			<h1>
				Balinese Gamelan Semara Dana
			</h1>
			<p>
Gamelan is the name for a large tuned percussion orchestra from Indonesia. The Balinese gamelan
presented on this website is a gamelan Semara Dana, which comprises bronze gongs, gong-chimes,
metallophones, cymbals and drums, requiring about 25 players. Semara Dana (meaning 'generous
love') is a hybrid between the modern Gong Kebyar (meaning 'to flare up', referring to the explosive
changes in tempo and dynamics) and the older, rarer Semar Pegulingan. Unlike Gong Kebyar,
gamelan Semara Dana uses the full 7-tone pelog scale.

In traditional cyclic pieces, larger metallophones play the core melody, which is punctuated by
various gongs and embellished (with interlocking melodic and rhythmic patterns) by smaller
metallophones and gong-chimes. The ensemble is led by a pair of drums, augmented by small
cymbals.

The metallophones are played in pairs with one tuned slightly higher than the other, causing an
acoustic beating, which, reproduced throughout the several octaves of the gamelan, results in the
brilliant shimmering sonority characteristic of Balinese gamelan music.
			</p>
